# Comparison Operators

print(5 == 5)   # Equal to
print(5 != 3)   # Different to
print(4 > 2)    # Greater than
print(2 < 3)    # Less than
print(6 >= 6)   # Greater than or equal to
print(3 <= 4)   # Less than or equal to

# String can also be compared

print("apple" == "apple")  # Equal to
print("apple" != "banana") # Different to

# Chain comparison

print(1 < 2 < 3)  # True
print(3 > 2 > 1)  # True
print(1 < 2 > 1)  # False

# Is your age between 18 and 65?

age = 25

print(18 <= age <= 65)
